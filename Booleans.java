public class Booleans {
    public static void main(String[] args) {
        boolean isJavaFun = true;
        System.out.println(isJavaFun); // Output result shows = true
        boolean isJavaNotScientific = false;
        System.out.println(isJavaNotScientific); // Output result shows = false

        // Here boolean expression 
        System.out.println(5 > 7); // Output is false because 5 is not greater than 7
        int x = 10;
        System.out.println(x == 10); // true 
        System.out.println(10 == 15); // false
    }
}